    private void buscar(){
        try {
            String nombreComida = jtfNombre.getText();
            Alimentos alimento = alimentoData.buscarAlimento(nombreComida);
            
            
            if (alimento != null) {
                jtfCodComida.setText(alimento.getCodComida() + "");
                jtfNombre.setText(alimento.getNombreComida());
                jtfCalorias.setText(String.valueOf(alimento.getCaloriasPorPorcion()));
                jcbTipo.setSelectedItem(alimento.getStipoComida());
                jtfDetalle.setText(alimento.getDetalle());
                
                DefaultTableModel model = (DefaultTableModel) jtAlimento.getModel();
                model.setRowCount(0);
                
                model.addRow(new Object[]{
                alimento.getCodComida(),
                alimento.getNombreComida(),
                alimento.getCaloriasPorPorcion(),
                alimento.getStipoComida(),
                alimento.getDetalle()
            });
        
            } else {
                JOptionPane.showMessageDialog(this, "Alimento no encontrado.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Código no válido.");
        }
    }

    public void limpiar(){
    
        jtfCodComida.setText("");
        jtfNombre.setText("");
        jtfDetalle.setText("");
        jtfCalorias.setText("");
        jcbTipo.setSelectedIndex(-1);
    }
    
    
    private void guardar(){
        try {
            int codComida = Integer.parseInt(jtfCodComida.getText());
            String nombre = jtfNombre.getText();
            int calorias = Integer.parseInt(jtfCalorias.getText());
            String tipo = (String) jcbTipo.getSelectedItem();
            String detalle = jtfDetalle.getText();

            Alimentos alimento = new Alimentos(codComida,nombre, calorias, tipo, detalle);
            alimentoData.guardarAlimento(alimento);
            JOptionPane.showMessageDialog(this, "Alimento guardado exitosamente.");
            actualizarTabla();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error en el ingreso de datos.");
        }    
    }
    
    private void modificar(){
        
        try {
            int codComida = Integer.parseInt(jtfCodComida.getText());
            String nombre = jtfNombre.getText();
            int calorias = Integer.parseInt(jtfCalorias.getText());
            String tipo = (String) jcbTipo.getSelectedItem();
            String detalle = jtfDetalle.getText();

            Alimentos alimento = new Alimentos(codComida, nombre, calorias, tipo, detalle);
            alimentoData.actualizarAlimento(alimento);
            JOptionPane.showMessageDialog(this, "Alimento modificado exitosamente.");
            actualizarTabla();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Código no válido.");
        }
    }  
    
    private void eliminar(){
        try {
             int codComida = Integer.parseInt(jtfCodComida.getText());
            alimentoData.eliminarAlimento(codComida);
            JOptionPane.showMessageDialog(this, "Alimento eliminado exitosamente.");
            actualizarTabla();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Código no válido.");
        }
    }
    
    private void actualizarTabla() {
        
        DefaultTableModel model = (DefaultTableModel) jtAlimento.getModel();
        model.setRowCount(0);
        
        for (Alimentos alimento : alimentoData.listarAlimentos()) {
            model.addRow(new Object[]{
                alimento.getCodComida(),
                alimento.getNombreComida(),
                alimento.getCaloriasPorPorcion(),
                alimento.getStipoComida(),
                alimento.getDetalle()
            });
        }
    }
